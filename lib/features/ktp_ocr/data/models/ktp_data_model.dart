import "../../domain/entity/ktp_data_entity.dart";

class KtpDataModel extends KtpDataEntity {
  const KtpDataModel({
    required String? nik,
    required String? namaLengkap,
    required String? tempatLahir,
    required String? tanggalLahir,
    required String? jenisKelamin,
    required String? golDarah,
    required String? alamatFull,
    required String? alamat,
    required String? rtrw,
    required String? kelDesa,
    required String? kecamatan,
    required String? agama,
    required String? statusPerkawinan,
    required String? pekerjaan,
    required String? kewarganegaraan,
    required String? berlakuHingga,
  }) : super(
          nik: nik,
          namaLengkap: namaLengkap,
          tempatLahir: tempatLahir,
          tanggalLahir: tanggalLahir,
          jenisKelamin: jenisKelamin,
          golDarah: golDarah,
          alamatFull: alamatFull,
          alamat: alamat,
          rtrw: rtrw,
          kelDesa: kelDesa,
          kecamatan: kecamatan,
          agama: agama,
          statusPerkawinan: statusPerkawinan,
          pekerjaan: pekerjaan,
          kewarganegaraan: kewarganegaraan,
          berlakuHingga: berlakuHingga,
        );

  factory KtpDataModel.fromJson(Map<String, dynamic> json) => KtpDataModel(
        nik: json['nik'],
        namaLengkap: json['nama_lengkap'],
        tempatLahir: json['tempat_lahir'],
        tanggalLahir: json['tanggal_lahir'],
        jenisKelamin: json['jenis_kelamin'],
        golDarah: json['gol_darah'],
        alamatFull: json['alamat_full'],
        alamat: json['alamat'],
        rtrw: json['rtrw'],
        kelDesa: json['kel_desa'],
        kecamatan: json['kecamatan'],
        agama: json['agama'],
        statusPerkawinan: json['status_perkawinan'],
        pekerjaan: json['pekerjaan'],
        kewarganegaraan: json['kewarganegaraan'],
        berlakuHingga: json['berlaku_hingga'],
      );

  Map<String, dynamic> toJson() => {
        "nik": nik,
        "nama_lengkap": namaLengkap,
        "tempat_lahir": tempatLahir,
        "tanggal_lahir": tanggalLahir,
        "jenis_kelamin": jenisKelamin,
        "gol_darah": golDarah,
        "alamat_full": alamatFull,
        "alamat": alamat,
        "rtrw": rtrw,
        "kel_desa": kelDesa,
        "kecamatan": kecamatan,
        "agama": agama,
        "status_perkawinan": statusPerkawinan,
        "pekerjaan": pekerjaan,
        "kewarganegaraan": kewarganegaraan,
        "berlaku_hingga": berlakuHingga,
      };
}
